// Helper function to get questions for a specific category and day
export const getQuestionsForCategory = (category, day) => {
  console.log('getQuestionsForCategory called with:', { category, day });
  
  const categoryMap = {
    entrepreneur: entrepreneurQuestions,
    managerial: managerialQuestions,
    technician: technicianQuestions
  };
  
  const questionsData = categoryMap[category];
  console.log('questionsData:', questionsData ? 'found' : 'not found');
  
  if (!questionsData) {
    console.error(`Category "${category}" not found`);
    return null;
  }
  
  const dayData = questionsData[day];
  console.log(`Day ${day} data:`, dayData ? 'found' : 'not found');
  
  if (!dayData) {
    console.error(`Day ${day} not found for category ${category}`);
    return null;
  }
  
  console.log(`Returning ${dayData.questions?.length || 0} questions`);
  return dayData;
};

